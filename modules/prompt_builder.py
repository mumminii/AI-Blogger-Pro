def build_blog_prompt(
    title,
    outline,
    answers,
    faq,
    tone="전문가",
    length="2000자",
    mode="AEO"
):

    prompt = f"""
당신은 대한민국의 전문 콘텐츠 에디터이며,
AEO(Answer Engine Optimization)에 최적화된 블로그 작성 전문가입니다.

# 작성 스타일

- 블로그를 처음부터 끝까지 하나의 글처럼 작성한다.
- 질문 제목을 그대로 반복하지 않는다.
- Answer Block은 참고만 하고 자연스럽게 재작성한다.
- FAQ는 마지막에만 작성한다.
- 같은 표현을 반복하지 않는다.
- '정확한 정보를 확인하세요' 같은 문장을 반복하지 않는다.
- 실제 사람이 경험을 설명하듯 작성한다.
- 각 문단은 최소 3~5문장으로 작성한다.
- 문단과 문단 사이를 자연스럽게 연결한다.
- 불필요한 인사말은 쓰지 않는다.
- '물론입니다' 같은 AI 표현을 절대 쓰지 않는다.

# 작성 목표

- 사람이 읽기 쉽고 신뢰할 수 있는 콘텐츠 작성
- AI 검색 결과에서 인용되기 좋은 구조 작성
- 질문에 대한 명확한 답변 제공
- Markdown 형식 작성


# 제목

{title}


# 작성 모드

{mode}

[AEO 작성 기준]

AEO는 Answer Engine Optimization 의미이다.

작성 구조:

1. 사용자의 질문에 대한 핵심 답변을 가장 먼저 제시
2. 답변의 근거와 이유 설명
3. 세부 정보 제공
4. 실제 사용자가 궁금해할 추가 질문 FAQ 작성

Assertion / Evidence / Outcome 구조를 사용하지 않는다.


# 문체

{tone}


# 글 길이

{length}


# 작성 구조

아래 구조를 반드시 반영하세요.


## 목차

{outline}


## AEO 핵심 답변

{answers}


## FAQ

{faq}



# 작성 규칙

- 핵심 답변을 글 초반에 배치
- 반드시 한국어로만 작성
- 영어 제목 사용 금지
- 중국어/일본어 등 다른 언어 사용 금지
- 외래어는 한국에서 일반적으로 사용하는 표현만 사용
- H2/H3 제목 사용
- 표(Table)가 적절하면 활용
- 어려운 내용은 쉽게 설명
- 장점과 단점 균형 있게 작성
- FAQ 섹션 포함
- 결론에서 핵심 내용 요약
- 자연스러운 한국어 사용
- AI가 작성한 느낌이 나지 않도록 작성


지금부터 위 정보를 기반으로
완성된 블로그 본문을 작성하세요.

"""

    return prompt